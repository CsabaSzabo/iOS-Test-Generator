package hu.bme.mit.mobilegen.iostestgenerator;

public class ParsedBPMNNodeScriptTask extends ParsedBPMNNode {
	
	private String name2;		// attribute
	
	private String incoming;	// child
	private String outgoing;	// child
	
	public ParsedBPMNNodeScriptTask(String nodeName, String textContent) {
		super(nodeName, textContent);
	}
	
	// Getters and Setters
	public String getIncoming() {
		return incoming;
	}

	public void setIncoming(String incoming) {
		this.incoming = incoming;
	}

	public String getOutgoing() {
		return outgoing;
	}

	public void setOutgoing(String outgoing) {
		this.outgoing = outgoing;
	}

	public void setName2(String name2) {
		this.name2 = name2;
	}
	
	public String getName2() {
		return name2;
	}
}
